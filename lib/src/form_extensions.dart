/// Common form validators
class Validators {
  /// Required field validator
  static String? required(String? value, {String? message}) {
    if (value == null || value.isEmpty) {
      return message ?? 'This field is required';
    }
    return null;
  }

  /// Email validator
  static String? email(String? value, {bool required = true}) {
    if (value == null || value.isEmpty) {
      return required ? 'Email is required' : null;
    }
    if (!RegExp(r'^[\w-\.]+@([\w-]+\.)+[\w-]{2,4}$').hasMatch(value)) {
      return 'Enter a valid email';
    }
    return null;
  }

  /// Min length validator
  static String? minLength(String? value, int length, {String? message}) {
    if (value == null || value.isEmpty) return null;
    if (value.length < length) {
      return message ?? 'Must be at least $length characters';
    }
    return null;
  }

  /// Max length validator
  static String? maxLength(String? value, int length, {String? message}) {
    if (value == null || value.isEmpty) return null;
    if (value.length > length) {
      return message ?? 'Must be at most $length characters';
    }
    return null;
  }

  /// Number range validator
  static String? numberRange(String? value, {double? min, double? max}) {
    if (value == null || value.isEmpty) return null;
    final number = double.tryParse(value);
    if (number == null) return 'Enter a valid number';
    if (min != null && number < min) return 'Must be at least $min';
    if (max != null && number > max) return 'Must be at most $max';
    return null;
  }

  /// Phone number validator
  static String? phone(String? value, {bool required = true}) {
    if (value == null || value.isEmpty) {
      return required ? 'Phone number is required' : null;
    }
    if (value.length < 10) {
      return 'Enter a valid phone number';
    }
    return null;
  }

  /// URL validator
  static String? url(String? value, {bool required = true}) {
    if (value == null || value.isEmpty) {
      return required ? 'URL is required' : null;
    }
    if (!RegExp(
      r'^https?:\/\/(www\.)?[-a-zA-Z0-9@:%._\+~#=]{1,256}\.[a-zA-Z0-9()]{1,6}\b([-a-zA-Z0-9()@:%_\+.~#?&//=]*)$',
    ).hasMatch(value)) {
      return 'Enter a valid URL';
    }
    return null;
  }

  /// Combine multiple validators
  static String? Function(String?) combine(
    List<String? Function(String?)> validators,
  ) {
    return (value) {
      for (final validator in validators) {
        final result = validator(value);
        if (result != null) return result;
      }
      return null;
    };
  }
}
