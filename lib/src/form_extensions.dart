/// Form wrapper extension
extension FormExtensions on Widget {
  /// Wrap widget in a Form with optional key and callbacks
  Widget form({
    Key? key,
    GlobalKey<FormState>? formKey,
    AutovalidateMode? autovalidateMode,
    void Function()? onChanged,
  }) {
    return Form(
      key: formKey ?? key,
      autovalidateMode: autovalidateMode,
      onChanged: onChanged,
      child: this,
    );
  }
}

